def postprocess_taiwan(text: object) -> str:
    """Apply conservative Taiwan terminology to model-produced cue text.

    The operation neither parses nor emits SRT metadata.  Line breaks are
    preserved so a caller can safely replace only an existing cue's text.
    """

    value = str(text or "")
    for source, replacement in _TAIWAN_TERMS:
        value = value.replace(source, replacement)
    value = re.sub(r"(?<=[\u3400-\u9fff])[ \t]+(?=[\u3400-\u9fff])", "", value)
    value = re.sub(r"(?<=[\u3400-\u9fff]),(?=[\u3400-\u9fff])", "，", value)
    value = re.sub(r"(?<=[\u3400-\u9fff]);(?=[\u3400-\u9fff])", "；", value)
    value = re.sub(r"(?<=[\u3400-\u9fff]):(?=[\u3400-\u9fff])", "：", value)
    value = re.sub(r"[ \t]+([，。！？；：,.!?;:])", r"\1", value)
    return value


_ENGLISH_SENTENCE_WORD_RE = re.compile(
    r"(^|(?<=[.!?])\s+)([\"'“‘(\[]*)([a-z][A-Za-z'’]*)",
    re.MULTILINE,
)
_ENGLISH_LOWERCASE_I_RE = re.compile(
    r"\bi(?=\b|['’](?:m|d|ll|ve|re)\b)"
)


def postprocess_english(text: object) -> str:
    """Conservatively polish casing from the compact local model.

    FuguMT's subtitle-style training data often emits an otherwise correct
    English sentence entirely in lowercase.  Only sentence-leading ASCII
    words and the standalone first-person pronoun are changed; spelling,
    wording, punctuation, and line breaks remain untouched.
    """

    value = _ENGLISH_LOWERCASE_I_RE.sub("I", str(text or ""))

    def capitalize_sentence_start(match: re.Match[str]) -> str:
        prefix, opening, word = match.groups()
        # Preserve intentionally styled names such as eBay and iPhone.
        if any(character.isupper() for character in word[1:]):
            return match.group(0)
        return prefix + opening + word[0].upper() + word[1:]

    return _ENGLISH_SENTENCE_WORD_RE.sub(capitalize_sentence_start, value)


