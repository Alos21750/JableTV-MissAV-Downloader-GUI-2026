def _build_memory(rows: tuple[tuple[str, str, str], ...]) -> dict[str, tuple[str, str]]:
    memory: dict[str, tuple[str, str]] = {}
    for source, english, taiwan in rows:
        key = normalize_cue(source)
        value = (english, taiwan)
        existing = memory.get(key)
        if existing is not None and existing != value:
            raise ValueError(f"Conflicting subtitle-domain phrase: {source!r}")
        memory[key] = value
    return memory


_PHRASE_MEMORY = _build_memory(_PHRASES)
_NONLEXICAL_MEMORY = _build_memory(_NONLEXICAL)
PHRASE_COUNT = len(_PHRASE_MEMORY)
NONLEXICAL_COUNT = len(_NONLEXICAL_MEMORY)


def _target_index(target_language: object) -> Optional[int]:
    key = _language_key(target_language)
    if key in _TARGET_ENGLISH:
        return 0
    if key in _TARGET_TAIWAN:
        return 1
    return None


def normalize_nonlexical(text: object, target_language: object) -> Optional[str]:
    """Translate a known non-lexical cue without guessing at unknown sounds."""

    target_index = _target_index(target_language)
    if target_index is None:
        return None
    translation = _NONLEXICAL_MEMORY.get(normalize_cue(text))
    return translation[target_index] if translation is not None else None


def exact_translation(
    text: object,
    source_language: object,
    target_language: object,
) -> Optional[str]:
    """Return a trusted whole-cue override, or ``None`` for the local model.

    No substring search, stemming, edit distance, or other fuzzy behavior is
    used.  This is particularly important for Japanese negation: a longer
    unknown cue containing ``やめて`` must not inherit the meaning "Stop."
    """

    if _language_key(source_language) not in _SOURCE_JAPANESE:
        return None
    target_index = _target_index(target_language)
    if target_index is None:
        return None

    nonlexical = _NONLEXICAL_MEMORY.get(normalize_cue(text))
    if nonlexical is not None:
        return nonlexical[target_index]
    translation = _PHRASE_MEMORY.get(normalize_cue(text))
    return translation[target_index] if translation is not None else None


