_SPACE_RE = re.compile(r"\s+")
_SPACE_BEFORE_PUNCTUATION_RE = re.compile(r"\s+([,.;:!?、。！？])")
_SPACE_AFTER_OPENING_RE = re.compile(r"([「『（(\[]) +")
_SPACE_BEFORE_CLOSING_RE = re.compile(r" +([」』）)\]])")
_DECORATIVE_EDGE_RE = re.compile(r"^[.!…~〜～]+|[.!…~〜～]+$")
